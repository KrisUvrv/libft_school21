#include "libft.h"
#include <limits.h>

int	ft_atoi(const char *str)
{
	unsigned int long long	num;
	int						sign;

	num = 0;
	sign = 1;
	while (*str == '\f' || *str == '\n' || *str == '\r'
		|| *str == '\t' || *str == '\v' || *str == ' ')
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + *str++ - '0';
		if (LLONG_MAX / 10 < num)
		{
			if (sign == -1)
				return (0);
			return (-1);
		}
	}
	return (num * sign);
}
